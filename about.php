        <h1>About</h1>
        <p>
            The City of Palo Alto hopes to condense a allocated 25-year road infrastructure repair process into a 5-year period, taking advantage of current resources and low costs.
</p><p>
	This site is a tool for the people of Palo Alto to better understand the conditions of the roads they and their children travel on everyday and how the City of Palo Alto is working to improve them.</p>
	<p>Entering an address will bring up that segment of road's score report based on a comprehensive assessment of street and sidewalk conditions, cracking, obstructions, strength of materials, etc. Residents can then see where that road falls in the City's plan to bring all roads up to a score of at least 60, with roads receiving grades of 0 to 30 especially targeted as high priority for repair.
